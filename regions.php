        <h3><b>Americas</b></h3>   
        
        <h5 class= 'region'><b>Hawaii</b></h5>
        <img class= 'map'; src= 'cpics/america.JPG' />
        <p>Notable as the only place in The United States that grows coffee, production in Hawaii
        is centered on the Kona mountain. Kona coffee fetches high prices, due to limited growing
        area and high demand. Growers have been expanding their plantings in Kona for many years,
        leading to reduced quality as they plant in less ideal areas. </p>
    
        <h5 class= 'region'><b>Mexico</b></h5> 
        <p>Mexico is a major coffee producing nation. Some Mexican coffee is extremely good,
        much of the best coming from Chiapas. The best Mexican coffee has a rich, nutty aroma
        and taste, often with a delicate but pronounced acidity. Despite a fair amount of production
        and a fairly reliable quality, Mexican coffee is not among the most common coffees carried
        as varietals. It is often used by roasters in blends.</p>

        <h5 class= 'region'><b>Guatemala, Costa Rica, Nicuragua </b> </h5>
        <p>These are generally high quality, well balanced coffees. They can be, in my opininon,
        among the best coffees, with a rich aroma, a well rounded complex body, and a distinctive
        acidity. Central American coffees are generally very similar to their much more famous Colombian
        and Jamaican relatives.</p>

        <h5 class= 'region'><b>Colombia </b> </h5>     
        <p>Famous for its well balanced, mild coffees, Colombia is a major coffee producing
        region. Generally of lower quality than other high quality coffees at a given price point,
        Colombia is nevertheless popular and decent quality.</p>

        <h5 class= 'region'><b>Brazil </b> </h5>
        <p>The best Brazilian coffees are outstanding: nutty, rich, spicy, and delicate with an
        aroma that is hard to match in any coffee. Sadly, Brazil is the largest producer of low
        quality coffees in the world. Most Brazil coffee has an unfortunate sharp bitter note that
        comes through any blend it goes into. Low quality Brazil is often used in blends by coffee
        roasters trying to cut corners. </p>
        
        <h5 class= 'region'><b>Jamacia</b></h5>
        <p>Jamacian Blue Mountain is among the most expensive coffees in the world, and among the
        most famous. Famed for it's balance and mild nature, I have always found it to be of above
        average quality, but sadly never living up to it's hype. Guatemalas and Costa Ricas are
        often of equal quality, and at a consistantly much lower price.</p>
    
        <h5><b>Africa</b></h5>    
        <h5 class= 'region'><b>Ethopia</b></h5>
        <img class= 'map' src='cpics/africa.JPG' />
        <p>Ethiopia really should be divided into three groups, as there are three distinctive coffee
        growing regions in Ethiopia: Harrar, Yirgacheffe, and Sidamo.</p>

        <h5 class= 'region'><b>Kenya</b></h5>
        <p>Fruity and sharp, an intense acidity and a weak body are the hallmarks of
        Kenyan coffee. Very popular, often going into espresso blends, the Kenyan crop
        often sells out as soon as it's harvested. Many smaller producers substitute coffee from
        Burundi, which has a very similar flavor profile, but is often less expensive. </p>
       
        <h5><b>Asia</b></h5>
        <h5 class= 'region'><b>Sumatra</b></h5>
        <img class= 'map' src='cpics/asia.JPG' />
        <p>Complex, earthy, rich aroma, full body and weak acidity are the marks of Sumatra coffee.
        The best Sumatra is of the Mandheling variety, which has a unique wet process for depulping the beans.
        Because it has such a strong body, many producers want to roast Sumatra to an extremely dark roast. The
        body will stand up to this, but its already weak acidity will be completely cooked away, leaving a cup
        with a very strong bittersweet body and virtually no acidity at all. I do not recommend taking Sumatra
        to a dark roast. At a light roast, Sumatra's naturally strong body will leave a cup that tastes like a
        darker roast than it is, while preserving its delicate acidity.</p>
        
        <h5 class= 'region'><b>Papua New Guinea</b></h5>
        <p>Earthy, with a heavy body and a rich aroma, PNG is generally a workhorse coffee. It tends to be very
        stable from season to season, making it a common addition to blends. It doesn't have the complex body
        that Sumatra does, but also has a stronger acidity.</p>
        
        <h5 class= 'region'><b>Java</b></h5>
        <p>Very similar to PNG, but generally a little less expensive. This was once such a famed varietal
        that its name is a synonym for coffee to this day. Sadly, climate change and overproduction have
        left Java coffee a pale shadow of what it once was. </p>
        
        <h5 class= 'region'><b>India</b></h5>
        <p>There is a small amount of coffee that comes out of India, much of it fairly standard
        Asian fare, not of particularly high quality. There is one variety in particular, Monsoon Malabar,
        which is processed uniquely. After drying, it is bagged and stacked in drafty warehouses during the
        Indian Monsoon season. This is a highly unconventional way to store coffee, as climate control and
        dry, cool conditions are key to keeping green coffee in good condition. In this case, however, it
        lends the coffee a unique note of sandalwood, and leads to a very interesting cup of coffee.</p>
