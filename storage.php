        <h3> Care and storage of coffee </h3>
        <img class= 'bigLeft' src='cpics/beans01.JPG' title= 'Fresh out of the roaster'>
        <p> Many people store their coffee poorly or incorrectly. Green coffee can store
        for months with no problems, but once roasted, coffee beans have a very limited lifespan.
        If left in whole beans, coffee will last a couple of weeks after it's roasted. If ground,
        it will last less long than that, based on the greater surface area exposed to the air. Ground coarsely,
        as for a French press, it will age about 100 times faster than in whole bean
        form (as it has aound 100
        times the surface area now) -- so it will start to become noticeably stale in several hours.
        Ground very finely, as for espresso, it will age about a thousand times faster,
        (again, now having around a thousand times the surface area), so it should be brewed within a minute or
        so.  </p>
        <p>Freshly roasted coffee needs to "outgas".
        This starts very rapidly, and tapers off slowly over the next few days.
        As it stales, it releases carbon dioxide gas. It only stops once it
        is fully stale. That is why the coffee you buy at the grocery store has vents in the bags.
        The contents would inflate and eventually rupture the bags without a pressure
        release. Any coffee that you purchase in a can is already fully stale; it must be made
        completely stale before it can be packaged into a can. </p>
        <p>You can delay this action by freezing your coffee. I don't really recommend freezing, as it
        damages the coffee beans, but if you need to keep your coffee for more than a couple of
        weeks, freezing is better than leaving it out. On the other hand, moisture is the worst
        thing for your beans, and the condensation from the cold air in the refrigerator is far more
        damaging to your coffee beans than being left to outgas, so never put your coffee in the
        refrigerator.</p>  <p><span class= 'note'> Pro tip: The outgassing of extremely fresh coffee
        beans will create a severe turbulence when they are exposed to water. The more finely ground the
        coffee is, the more significant the sudden burst of outgassing will be. This means that extremely fresh
        coffee will make very poor espresso, as it will behave unpredictably in the portafilter. To make
        espresso, beans should age for three to five days before they are used.</span></p>
