        <h3>Random thoughts about coffee and myths surrounding it.</h3>
        <h3>Dishonesty in the Coffee Industry</h3>
        <p>There is a shocking amount of foolishness, misinformation, and outright lies that go around the coffee
        industry. Honestly, there are too many to list here. Many people make claims about coffee which anyone with critical
        thinking skills could see were patently false, yet I see them repeated over and over. I strongly encourage anyone
        who is even slightly interested in coffee to do a small amount of research, and read a book or two about it. </p> 
        
        <h3>Acidity</h3>
        <p>I do feel the need to address one of the biggest and most common points of dishonesty surrounding coffee.
        Most people have foolish ideas about the amount of acid in coffee. Sadly, this is
        generally not helped, but instead perpetuated by the coffee industry. Acidity in coffee is the measure of a particular flavor.
        The sharp, dry, sparkly bite at the front of your tongue as you sip coffee is the acidity.
        It commonly tastes of citrus fruit or flowers. It generally corelates moderatly to the actual pH of that coffee, though coffee is
        actually less acidic than most common beverages. </p>
        <img id ='ph' src= 'cpics/pHchart.png' />
        <p> Here is a chart with the pH levels of some common items.
        As you can see, black coffee has a pH around 5. Tomatoes have about a 4
        on that same scale, and most sodas have about a 2.5.  Remember that pH is a logarithmic scale, meaning a tomato has about 10x as much acid as an
        equivalent size cup of black coffee. Six ounces of soda, however, has a whopping 500 times the amount
        of acid that a cup of coffee does. <a class= 'noDeco' href=http://www.coffeeresearch.org/science/sourmain.htm>Here is an article
        that goes into much more detail on acidity in coffee.</a> </p>
        
        <h3>Reccomended further reading</h3>
        <p>After telling you that you shouldn't just take someone's word on it for facts, I should probably
        give you some tools to help you come to your own conclusions, rather than expecting you to take my word for it.
        Here are some good sources, though this is by no means an exhaustive list.</p>
        <p> <a class= 'noDeco' href= 'http://theoatmeal.com/comics/coffee'>This Oatmeal cartoon should be your first stop.</a>
        Not only funny but also an informative infographic.</p>
        <p><a class= 'noDeco' href= 'http://www.scaa.org/'>The Specialty Coffee Assocation of America</a> is an industry organization
        that establishes a lot of the standards coffee shops use. If you're not a professional, there
        is a limited amount to interest you at their site, but they have some useful information about
        standards used in the industry.</p>
        <p><a class= 'noDeco' href= 'http://www.amazon.com/Cuppers-Handbook-Systematic-Sensory-Evaluation/sim/B007YY0WIK/2'>
            The Coffee Cuppers Handbook</a> is an extremely useful resource if you are tasting and grading
        coffee professionally. It is a moderately useful resource if you like coffee and would like to have
        a better vocabulary for discussing flavors with your friends.</p>
        <p><a class= 'noDeco' href= 'http://www.amazon.com/Coffee-Roasting-Revised-Updated-Edition/dp/0312312199'>
            Home Coffee Roasting</a> is a book which will tell you all about roasting coffee at home, in your
        own kitchen. It's not too hard to do, but you will never end up with quite the quality of coffee that
        a small batch roaster can. Don't let that discourage you, though, it's fun and rewarding nonetheless.</p>
        <p><a class= 'noDeco' href= 'http://www.amazon.com/Uncommon-Grounds-History-Coffee-Transformed/dp/046501836X/ref=sr_1_1?ie=UTF8&qid=1412360143&sr=8-1&keywords=uncommon+grounds'>
        Uncommon Grounds</a> is probably the single most useful book on coffee out there. It details a history
        of coffee from its mythical origins to the ways in which it is moved and traded today. It includes
        sections on the coffee industry and common varietals.
        </p>
        <p><a class= 'noDeco' href='http://www.ncausa.org/i4a/pages/index.cfm?pageid=69'>How coffee is
        processed</a> An article with pretty good information and photos about coffee planting, harvesting,
        and processing. I would like to talk about that more, but it is really beyond the scope of this web page.
        </p>
