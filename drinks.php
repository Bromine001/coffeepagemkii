               <dd class = Americano><p><img class= 'imLeft' src = 'cpics/coffeeCream.JPG' title= 'looks just like a regular old cup
               of coffee.' alt= "Americano in a glass cup" />
               Espresso mixed with hot water, to approximate the strength of brewed coffee. Common dilutions
               range anywhere from 1:2 to 1:10 of espresso to water. Originally invented in Italy, where American
               soldiers on leave wanted cups of brewed coffee, but the Italian cafes only had espresso machines.</p>
