            <dt class= 'default'>Breve</dt>
                <dd class = Breve><p>Technically any coffee drink made with half and half, more typical usage is a latte
                made with half and half instead of milk.</p>
                </dd>  
            <dt class= 'default'>Cafe au lait</dt>
                <dd class = 'Cafe Au lait'><p>Brewed coffee topped with steamed milk. Generally served in a 1:1 ratio.</p>
                </dd>
            <dt class= 'default'>Cowboy coffee</dt>
                <dd class = 'Cowboy coffee'><p>A method of making coffee, where water is boiled, then grounds added and allowed to settle
                but not removed before drinking. There are various methods for minimizing the grounds in the cup
                including cracking a raw egg into the pot, adding an ice cube, and tapping the side of the pot.</p>
                <p>None of these methods is particularly effective. Due to the continued presence of grounds,
                we don't recommend this method of preparation. A far superior method for making coffee in the
                outdoors is detailed <a class = 'noDeco' href='colador.php'>in this article about coladors.</a></p>
                </dd>  
            <dt class= 'default'>Cubano</dt>
                <dd class = 'Cubano'><p>Specifically it is espresso sweetened with sugar added into the portafilter.</p>
                <p><span class = 'note'> Pro tip: This is a foolish practice which not only makes a terrible
                shot of espresso, but will wear out an espresso machine prematurely. A very similar effect can
                be more effectively achieved by pouring sugar into the shot glass to dissolve as the espresso drips
                out.</span></p>
                </dd>
            <dt class= 'default'>Cappuccino</dt>
                <dd class = 'Cappuchino'><p><img class= 'bigRight' src= 'cpics/capp.JPG' title= "Cappuchino in it's native environment"
                                                 alt = "Cappuchino in china"/>
                A mix of espresso and steamed milk. Generally uses much stiffer foam than a latte.
                Traditionally cappuccinos are served in a 1:1:1 ratio between espresso: liquid milk: milk foam.
                This generates a 5-6 ounce drink. In the US, a cappuccino is often served in a 16 or 20 ounce size,
                generally with one or two shots of espresso, much more like a stiff latte than a traditional
                cappuccino.</p>
                </dd>    
            <dt class= 'default'>Cortado</dt>
                <dd><p>Espresso mixed with warm milk in a 1:1 ratio. Often drunk by people who like espresso but
                don't care for the strong flavor of straight espresso.</p>
                </dd>
            <dt class= 'default'>Doppio</dt>
                <dd><p>Italian for "double". Used in the US as a pretentious term for a double
                shot of espresso.</p>
                </dd>
            <dt class= 'default'>Espresso</dt>
                <dd><p><img class= 'bigLeft' src= 'cpics/espresso1.JPG' title = "Double shot of espresso"
                                               alt= "Double shot of espresso"/> The base of nearly every coffee drink.</p>
                <p>Good espresso is intensely bittersweet, rich and smooth. Neat espresso is not a drink that everyone
                enjoys, but I reccomend at least trying it to anyone. </p>
                <p>Bad espresso, on the other hand, is bitter, harsh and sharp tasting. Sadly, even in most coffee shops,
                bad espresso what is served.</p>
                See the section on
                <a class = "noDeco" href = "espresso.php">espresso</a> for more information.</p>
                </dd>
            <dt class= 'default'>Flat white</dt>
                <dd class = 'Flat white'><p>A latte with no foam. Popular in Australia, becoming popular in the US also.
                Some people will tell you that a flat white will walk your dog, clean your kitchen, pay your bills,
                and pay your student loan debts. I'm not sure why they say that, it's just a latte.</p>
                </dd>
            <dt class= 'default'>French Press</dt>
                <dd><p>An extraction method rather than a drink per se. In a French press (or Bodum, or plunger press),
                the grounds are separated using a fine metal screen rather than a paper filter. The resultant
                coffee is thicker, with a more robust body than the same coffee made in a drip maker.
                Click here for a <a class= 'noDeco' href='Press.php'>photo essay about making french press coffee</a>.</p>
                </dd>
