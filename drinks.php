            <dt class= 'default'>Instant coffee</dt>
                <dd class = 'Instant coffee'><p>Not a beverage. It is in fact, not fit for human consumption. Though it should be noted
                that instant coffee is not strictly unacceptable in recipes, though fresh brewed coffee is preferable even there.</p></dd>
            <dt class= 'default'>Latte (Cafe latte)</dt>
                <dd class = 'Latte (Cafe latte)'><p><img class='imLeft' src= 'cpics/latte5.JPG' alt = 'latte with art' title = 'Latte!'/>
                <img class= 'imRight' alt = 'Latte action shot!' title = 'Latte side view' src= 'cpics/latteaction.JPG' />
                The most common coffee drink in the US. A latte is a mix of espresso and
                steamed milk. Traditionally in a 1:3 ratio, though more common in the US
                is anywhere between a 1:5 and 1:16 ratio. Often served with flavored sugar
                syrup or chocolate.</p>
                <p>Lattes are generally made with either one or two shots of espresso.
                Generally served in 8, 12, 16, or 20 oz. sizes.</p>
                </dd>
            <dt class= 'default'>"Low Acid" coffee</dt>
                <dd><p>A bulls**t marketing gimmick that preys on people who are misinformed about the
                difference between the flavor "acidity" and the pH of coffee. Coffee is far
                less acidic than Lemonade, Coca-Cola, or tomatoes. See my article about 
                <a class= 'noDeco' href='random.php'>acidity in coffee</a>.</p>
                </dd>                
            <dt id= 'mocha'>Mocha (Cafe mocha)</dt> 
                <dd> <p>A variant of a latte with chocolate added. These are served at a variety of sweetnesses
                and strengths, sometimes with dark chocolate, and sometimes with white chocolate.</p>
                <p>"Turtle" lattes are a variant made with caramel and chocolate.</p>
                <p>"Nutella" lattes are another variant made with hazlenut syrup and chocolate.</p>
                </dd>
            <dt class= 'default'>Macchiato</dt>
                <dd><p>Espresso with a scoop of milk foam on top. Usually either a single or double shot, served
                in a demitasse cup.</p>
                <p><span class = 'note'>Note: A "Caramel Macchiato" is a Starbucks drink, not a coffee shop
                drink. To get a Starbucks Caramel
                Macchiato in a different coffee shop, you should order a caramel vanilla latte.</span></p>
                </dd>
            <dt class= 'default'>Moka</dt>
                <dd><p><img class= 'imLeft' src= 'cpics/moka.JPG' title= 'moka pot' />
                Coffee made with a Moka pot. Similar to espresso, but made with much lower 
                pressure than espresso. Moka pots are sometimes sold as "stovetop espresso" machines.
                They are tricky to use, time consuming to clean, and they make a drink which is tasty, but
                certainly not espresso.</p>
                </dd>
            <dt class= 'default'>Pour over</dt>
                <dd><p><img class= 'bigRight' src= 'cpics/carafe.JPG' alt= "delicious pourover coffee" title= 'it has a little carafe!' />
                Like the French Press, a pour over is really an extraction method rather than a drink.
                A pour over is basically brewed coffee. It only differs in that it is made by hand, generally with
                a Melitta or a Hario V-60.</p>
                <p>Click here for a <a class= 'noDeco' href='pourover.php'>photo essay about making pour
                over coffee</a>.</p>
                </dd>    
            <dt id= 'redEye'>Percolator</dt>
                <dd><p>An old fashioned method for brewing coffee, rather than a drink. A percolator is
                a pot with a plate in the bottom hooked to a central tube that extends to the top of the pot.
                As water boils, it is forced up the tube to splash repeatedly over a basket of grounds which
                is also suspended in the pot above the level of the water.</p>
                <p>As this method involves re-circulating the coffee through the basket, and using water
                which is at 212 deg. We do not reccomend it as a preparation method. It tends to lead to a
                badly overextracted coffee, which is extremely bitter. </p>
                </dd>
            <dt class= 'default'>Red Eye (Green Eye, Black Eye, etc.)</dt> 
                <dd><p>Brewed coffee with espresso. Generally one shot for a Red Eye. Also called by various
                 names, such as "Shot in the Dark", "Black eye" or "Green eye".</p>
                <p><span class = 'note'>Pro tip: Your Barista hates it when you order something like a "Black Eye"
                or a "Green Eye" or some such. These are vague terms at best, and he (or she) can't tell what you are
                asking for.
                Your Barista loves it when you order is clear "A 16 oz. coffee with two shots of
                espresso and room for cream." </p> </span>
                </dd>
            <dt class= 'default'>Ristretto</dt>
                <dd><p>Espresso pulled "short", generally about .75 oz per shot rather than the usual 1 oz. 
                Generally generates a richer drink with more acidity and more floral or citrus notes and
                less bittersweet caramel flavor.</p>
                </dd>
